using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System;
 class IfElse
{
     static void Main(string[] args)
    {
        int num = 10;
        if (num % 2 == 0)
        {
            Console.WriteLine("It is an even number");
            Console.ReadLine();
        }
        else
        {
            Console.WriteLine("It is an odd number");
            Console.ReadLine();
        }

    }
}
